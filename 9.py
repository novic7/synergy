# Ввод натурального числа X
X = int(input("Введите натуральное число X: "))

# Инициализация счетчика делителей
count_divisors = 0

# Подсчет делителей
for i in range(1, int(X**0.5) + 1):
    if X % i == 0:  # Если i является делителем X
        count_divisors += 1  # Увеличиваем счетчик
        if i != X // i:  # Если i и (X // i) разные, считаем еще один делитель
            count_divisors += 1

# Вывод количества делителей
print("Количество натуральных делителей числа X:", count_divisors)