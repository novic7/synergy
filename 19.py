# Создаем пустой словарь для хранения информации о питомцах
pets = {}

# Функция для добавления питомца в словарь
def add_pet():
    # Вводим имя питомца
    pet_name = input("Введите имя питомца: ")
    
    # Вводим вид питомца
    pet_type = input("Введите вид питомца: ")
    
    # Вводим возраст питомца и преобразуем его в int
    pet_age = int(input("Введите возраст питомца: "))
    
    # Вводим имя владельца питомца
    owner_name = input("Введите имя владельца: ")
    
    # Добавляем информацию о питомце в словарь
    pets[pet_name] = {
        'Вид питомца': pet_type,
        'Возраст питомца': pet_age,
        'Имя владельца': owner_name
    }

# Запрашиваем информацию о питомце
add_pet()

# Выводим информацию о питомце
for pet_name, pet_info in pets.items():
    age_suffix = "лет"  # По умолчанию "лет"
    if pet_info['Возраст питомца'] % 10 == 1 and pet_info['Возраст питомца'] % 100 != 11:
        age_suffix = "год"
    elif pet_info['Возраст питомца'] % 10 in [2, 3, 4] and not (pet_info['Возраст питомца'] % 100 in [12, 13, 14]):
        age_suffix = "года"

    # Форматируем вывод
    print(f"Это {pet_info['Вид питомца']} по кличке \"{pet_name}\". Возраст питомца: {pet_info['Возраст питомца']} {age_suffix}. Имя владельца: {pet_info['Имя владельца']}.")

